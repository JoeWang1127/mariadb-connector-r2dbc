  @BeforeAll
  public static void before2() {
    sharedConn.createStatement("DROP TABLE StringTable").execute().blockLast();
    sharedConn
        .createStatement(
            "CREATE TABLE StringTable (t1 varchar(256)) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci")
        .execute()
        .blockLast();
    sharedConn
        .createStatement("INSERT INTO StringTable VALUES ('someðŸŒŸ'),('1'),('0'), (null)")
        .execute()
        .blockLast();
    // ensure having same kind of result for truncation
    sharedConn
        .createStatement("SET @@sql_mode = 'STRICT_TRANS_TABLES,NO_ENGINE_SUBSTITUTION'")
        .execute()
        .blockLast();
  }

